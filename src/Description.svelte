<p>
My keyboard is <a href="https://github.com/foostan/crkbd">Corne keyboard</a>. It is a 42 keys split keyboard with 3x6 column staggered keys and 3 thumb keys.

My main device is Mac and I use vanilla <a href="https://colemak.com/">Colemak</a> layout. This layout adopts a lot of ideas from <a href="https://github.com/manna-harbour/miryoku/">Miryoku layout</a>.

I use <a href="https://precondition.github.io/home-row-mods">Home Row Mod</a> with Shift, Control, Option, Cmd order. I can easily access these modifier keys by holding down the respective key.

Holding down these thumb keys will go into another layer:
- Space -> Navigation layer
- Esc -> Number layer
- Enter -> System layer
- Backspace -> Symbol layer
